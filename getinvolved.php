<h1>Getting involved :)</h1>



<p>

Are you interested in the developement of <a href="index.php">Gclusters</a> ? Great! You don't have to be an experd of HTML or PHP, and you don't need to spend a great amount of time. A big portion of the help I need is simply feedback and reports of problems and bugs.

<p>
Here are some possible way to help:
<p>
<ul>
<li>Report me bugs and any kind of problems and/or inconsistencies in the normal use of Gclusters,
<li>Did you notice an updated value of a certain parameter (or a new colour-magnitude diagram) in a published work, for one of the globular clusters in the database? Let me know!
</p>
</ul>
<p>
More tecnically-oriented people may be interested to know that it is possible to submit bugs and also to have access to the html/php code of the project itself, via the <a href="http://mcastel.indefero.net/p/gclusters/">online repository</a>. Please see <a href="http://globularclusters.wordpress.com/2009/01/27/gen-09-a-remote-repository-for-gclusters/">this blog post</a> for details. 
<p>
Persons interested - at any level - in helping the developement of Gclusters, are also kindly invited to
take part in the related <a href="http://scdb.projects.zoho.com/">Stellar Clusters Database</a> Project hosted at
<a href="http://projects.zoho.com">Zoho Project</a>. Just send me an email to be enrolled  ;)

